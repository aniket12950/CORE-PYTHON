#tuple with mixed datatypes
tuple1=(1,"hello",3.4)
print(type(tuple1))
print(tuple1)
#nested tuple
tuple2=("mouse",[8,4,6],(1,2,3))
print(tuple2)   
#using index to access tuple elements
tuple4=('p','e','r','m','i','t')
print(tuple4[0])
print(tuple4[5])
print(tuple4[-1])
print(tuple4[-6])
#slicing of tuple
print(tuple4[1:4])
print(tuple4[2:])   
print(tuple4[:4])
print(tuple4[:])
print(tuple4[-4:-1])
    
    